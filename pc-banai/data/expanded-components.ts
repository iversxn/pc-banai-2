// Helper functions to generate massive datasets
function generateMoreCPUs(count: number): Component[] {
  const cpus: Component[] = []
  const brands = ["Intel", "AMD"]
  const sockets = ["LGA1700", "AM4", "AM5", "LGA1200"]
  const generations = ["12th Gen", "13th Gen", "5000 Series", "7000 Series"]

  for (let i = 0; i < count; i++) {
    const brand = brands[i % brands.length]
    const socket = sockets[i % sockets.length]
    const generation = generations[i % generations.length]

    cpus.push({
      id: `cpu-generated-${i}`,
      name: `${brand} ${generation} Processor ${i}`,
      nameBengali: `${brand} ${generation} প্রসেসর ${i}`,
      category: "cpu",
      brand,
      socket,
      specifications: {
        cores: 4 + (i % 20),
        threads: 8 + (i % 40),
        baseClock: `${2.5 + (i % 3)}GHz`,
        boostClock: `${4.0 + (i % 2)}GHz`,
        cache: `${16 + (i % 48)}MB L3`,
        tdp: `${65 + (i % 100)}W`,
        generation,
      },
      prices: generateRetailerPrices(15000 + i * 500, 8000 + i * 200),
      compatibility: { socket, maxTDP: 65 + (i % 100) },
      images: ["/placeholder.svg?height=200&width=200"],
      reviews: [],
      powerConsumption: 65 + (i % 100),
    })
  }

  return cpus
}

